print("=== Perhitungan Luas Permukaan Kubus ===\n")

# Input sisi kubus
sisi = float(input("Masukkan panjang sisi kubus: "))

# Rumus luas permukaan kubus = 6 × sisi²
luas_permukaan = 6 * (sisi ** 2)

# Menampilkan hasil
print(f"\nPanjang sisi kubus: {sisi} cm")
print(f"Luas permukaan kubus: {luas_permukaan} cm²")